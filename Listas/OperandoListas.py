#Criando uma lista
lista = [1, 2, 3, 4, 5]
#Imprimindo a lista
print(lista)

#Adicionando um elemento a lista
lista.append(6)
#Imprimindo a lista
print(lista)

#Removendo um elemento da lista
lista.remove(3)
#Imprimindo a lista
print(lista)

#Invertendo a lista
lista.reverse()
#Imprimindo a lista
print('Lista invertida', lista)

#Ordenando a lista
lista.sort()
#Imprimindo a lista
print('Lista ordenada', lista)
