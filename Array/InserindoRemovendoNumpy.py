import numpy

# Create a numpy array
arr = numpy.array([1, 2, 3, 4, 5])
# Print the array
print(arr)

# Print the shape of the array
print(arr.shape)

# Reshape the array
arr = arr.reshape(5, 1)
# Print the reshaped array
print(arr)

# Print the shape of the array
print(arr.shape)

# Transpose the array
arr = arr.T
# Print the transposed array
print(arr)

# Print the shape of the array
print(arr.shape)
